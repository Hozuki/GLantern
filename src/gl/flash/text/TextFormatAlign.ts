export abstract class TextFormatAlign {

    static get CENTER(): string {
        return "center";
    }

    static get END(): string {
        return "end";
    }

    static get JUSTIFY(): string {
        return "justify";
    }

    static get LEFT(): string {
        return "left";
    }

    static get RIGHT(): string {
        return "right";
    }

    static get START(): string {
        return "start";
    }

}
